
public class Lesson_66 {
	/*
	 * Obtaining the length of Strings
	 */

	public static void main(String[] args) {
		String car1 = "Ford";
		String car2 = "Chevy";
		String car3 = "Mitsubishi";
		String str = "I really love cars a great deal. No, I really don't have 3 cars.";

		System.out.println(car1);
		System.out.println(car1.length());
		
		System.out.println(car2);
		System.out.println(car2.length());
		
		System.out.println(car3);
		System.out.println(car3.length());
	
		System.out.println(str);
		System.out.println(str.length());
		
	}

}
