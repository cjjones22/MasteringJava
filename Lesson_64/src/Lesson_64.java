
public class Lesson_64 {
	/*
	 * Creating and using Strings
	 */

	public static void main(String[] args) {
		String str = new String("I love hamburgers.");
		String jason = new String("And I also love french fries");
		
		String str2 = "I like to go to Disney Land";
		String gibson = "45782 is my favorite number";
		
		System.out.println(str);
		System.out.println(jason);
		System.out.println();
		System.out.println(str2);
		System.out.println(gibson);
		System.out.println();

		System.out.println("Valentine's Day " + str);
		System.out.println(str + str2);

	}

}
