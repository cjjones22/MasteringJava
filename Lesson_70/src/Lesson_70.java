
public class Lesson_70 {
	/*
	 * Searching a string for a substring
	 */

	public static void main(String[] args) {
		String jason = "I love going to the grocery store";
		
		System.out.println(jason);
		System.out.println(jason.indexOf("grocery")); // gives beginning location of value
		System.out.println(jason.substring(20, 27));

	}

}
