
public class Lesson_74 {
	/*
	 * Changing a string to uppercase or lowercase
	 */

	public static void main(String[] args) {
		String input1 = "I REALLY LOVE THIS RESTAURANT";
		String input2 = "i also love to go on vacation";
		
		System.out.println(input1);
		System.out.println(input1.toLowerCase());
		
		System.out.println(input2);
		System.out.println(input2.toUpperCase());

	}

}
