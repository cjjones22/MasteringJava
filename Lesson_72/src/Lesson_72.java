
public class Lesson_72 {
	/*
	 * Searching a string from the end for a substring
	 */

	public static void main(String[] args) {
		String jason = "I love to go grocery shopping to get food.";
		
		System.out.println(jason);
		System.out.println(jason.indexOf("to"));
		System.out.println(jason.lastIndexOf("to"));

	}

}
